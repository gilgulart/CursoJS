/*
Operadores Lógicos
&& -> AND -> E
|| -> OR -> OU
! -> NOT -> NÃO
*/

// AND
console.log("AND");

console.log("Verdadeiro e Verdadeiro", true && true);
console.log("Vedadeiro e Falso",true && false);
console.log("Falso e verdadeiro",false && true);
console.log("Falso e Falso", false && false);
console.log("--------------------------------------");

// OR
console.log("OR");

console.log("Verdadeiro e Verdadeiro", true || true);
console.log("Vedadeiro e Falso",true || false);
console.log("Falso e verdadeiro",false || true);
console.log("Falso e Falso", false || false);

console.log("--------------------------------------");

// NOT
console.log("NOT");

console.log("Verdadeiro", ! true);
console.log("Falso" , ! false);

console.log("--------------------------------------");

